package WrapperClass;

public class WrapperClassDemo {

	public static void main(String[] args) {
		Integer iob=100;
		int a=iob;
		System.out.println(a+" "+iob);

		Character cob='A';
		char c=cob;
		System.out.println(c+" "+cob);

		Float fob=1.25f;
		float f=fob;
		System.out.println(f+" "+fob);
		
		Double dob=7.14;
		double d=dob;
		System.out.println(d+" "+dob);
		
		
	}

}
